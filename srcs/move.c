void	x_iter(char *s, int iter)
{
	int	i;

	i = 0;
	while (i < 8)
	{
		s[i] += iter;
		i += 2;
	}
}

void	y_iter(char *s, int iter)
{
	int	i;

	i = 1;
	while (i < 8)
	{
		s[i] += iter;
		i += 2;
	}
}

void	toleft(char *t)
{
	while (t[0] > '\0' && t[2] > '\0' && t[4] > '\0' && t[6] > '\0')
	{
		t[0]--;
		t[2]--;
		t[4]--;
		t[6]--;
	}
}

void	totop(char *t)
{
	while (t[1] > '\0' && t[3] > '\0' && t[5] > '\0' && t[7] > '\0')
	{
		t[1]--;
		t[3]--;
		t[5]--;
		t[7]--;
	}
}

void	totopleft(char *l)
{
	toleft(l);
	totop(l);
}
